package Day_14_NestedLoops;

public class Topic3_Example2 {
    public static void main(String[] args) {
        //stairs in number
        for (int i = 6; i >=0; i--) {
            for (int j = 0; j <=i; j++) {
                System.out.println(j+ " ");

            }
            System.out.println();

        }

    }
}
