package Day_7_Swich_Scanner;

public class Topic2_IfandElse {
    public static void main(String[] args) {
        // if is only used when the classes are not related
        // we need two things that are comparable with each other that is when we use if else.

   String cls="Java";
   if(cls!= "java"){
       System.out.println("Person is added to Java Class");

   }
   if(cls !="HTML"){

       System.out.println("Person is added to HTML Class");
   }

   if (cls!= "CSS"){

       System.out.println("Person is added to CSS Class");

   }

    }
}
