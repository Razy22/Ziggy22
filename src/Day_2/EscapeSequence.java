package Day_2;

public class EscapeSequence {
    public static void main(String[] args){
        // \t it is going to leave paragraph space on console

        System.out.print("\tThis is a tab space \tthis is   another one ");
        System.out.print("this is a test \t");


        // \n it is  going to move to a new line
        System.out.print("\nNew line");

        // \" print double quote " on the console
        System.out.print("\n This is \"ZIGGY\"");
        // \\ prints\ on the console
        System.out.print("\n this is back slash \\");

        // \' prints singe quote on the console
        System.out.print("\n This is \'ZIGGY\'");
    }
}
