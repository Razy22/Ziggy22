package Day_2;

public class Variables {
    public static void main(String[] args){
        boolean bolvalue= true;
        System.out.println(bolvalue);
        bolvalue=false;
        System.out.println(bolvalue);

        byte bytevalue= 120;
        System.out.println(bytevalue);

        char charvalue='W';
        System.out.println(charvalue);

        short shortvalue= 400;
        System.out.println(shortvalue);

        int intvalue= 4000000;
        System.out.println(intvalue);

        long longvalue= 123456l;
        System.out.println(longvalue);

        float floatvalue= 3.01f;
        float floatvalue2= 123.23f;
        System.out.println(floatvalue);
        System.out.println(floatvalue2);

        double doublevalue= 123.45678900;
        System.out.println(doublevalue);

    }
}
